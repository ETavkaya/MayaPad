export function buildTracksFromRoleOrder(
  roleOrder: TrackRoleId[],
  selectedTrackRole: TrackRoleId | null = roleOrder[0] ?? null,
): Track[] {
  return roleOrder.map((role, index) => {
    const def = TRACK_ROLE_LIBRARY[role]
    return {
      id: `track-${index + 1}`,
      index,
      role: def.role,
      label: def.label,
      shortLabel: def.shortLabel,
      color: def.color,
      icon: def.icon,
      acceptedCategories: [...def.acceptedCategories],
      preferredTypes: [...def.preferredTypes],
      keyNeutral: def.keyNeutral,
      liveInput: def.liveInput,
      inputType: def.inputType,
      futureDevice: def.futureDevice,
      allowOneShots: def.allowOneShots,
      launchRule: def.launchRule,
      hardwareColumnIndex: index,
      armed: false,
      muted: false,
      solo: false,
      selected: selectedTrackRole ? role === selectedTrackRole : index === 0,
      volume: 1,
      playingClipId: null,
      queuedClipId: null,
    }
  })
}

export function buildTracksFromPreset(presetName: LayoutPresetName): Track[] {
  return buildTracksFromRoleOrder(TRACK_LAYOUT_PRESET_ORDER[presetName])
}
